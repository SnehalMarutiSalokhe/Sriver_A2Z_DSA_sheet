class Solution
{
	public:
		string is_palindrome(int n)
		{
		    // Code here.
            int d=0,rev=0;
            for (int i = n; i >0; i=i/10)
            {
                d=i%10;
                rev= d+ rev*10;
            }
            if(rev==n) return "Yes";
            else return "No";
            
		}
};

